# length function
name = "harry potter"
print(len(name))
# string.endswith function
t = name.endswith("ry")
print(t)
# string.count() function
u = name.count("r")
print(u)

print(name.startswith("H"))

# string.capitalize
print(name.capitalize())

#string.replace
print(name.replace("t", "d"))

# string.find()
print(name.find("potter"))