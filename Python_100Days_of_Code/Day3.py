# Operators in Python:
"""
1. Arithmatic Operators: +, -, *, /, etc.
2. Assignment Operators: =, +=, -=, etc.
3. Comparison Operators: ==, >, >=, <, !=, etc.
4. Logical Operators: and, or, not. """

# Arithmatic Operators: 
a = 7
b = 4
c= a+b
print(c)

# Assignment Operators:
d = 4-2 #Assign 4-2 to d which is variable
print(d)
e = 6
e += 3
print(e) # increments the value of e by 3 and then assigns it to e

# Comparison Operators: Only return boolean values 'true' or 'false'
f = 5>4
print(f) # will return the 'true' or 'false' only

# Logical Operators: 
g= True or False
print(g)

