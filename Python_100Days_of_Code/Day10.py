# Methods: 
#Example:
l = [1, 8, 7, 2, 21, 15]
l.sort() # updates the list to [1, 2, 7, 8, 15, 21]
print(l)
l.append(8) # adds 8 at the end of the list
print(l)
l.reverse() # updates the list to [15, 21, 2, 7, 8, 1]
print(l)
l.insert(3, 8) # This will add 8 at index 3
print(l)
l.pop(2) # Will delete element at index 2 and return it's values
print(l)
l.remove(21) #will remove 21 from the list
print(l)

