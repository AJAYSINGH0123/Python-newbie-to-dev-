# Tuples in python: A immutable(cannot be changed) data type in python
# Consider the fillowing table: 
t = (1, 7, 2)
# Tuple methods:
print(t.count(1)) # will return number of times 1 occurs in a
print(t.index(1)) # will return the index of first occurence of 1 in a.
print(t)

